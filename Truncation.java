package com.firstprogram.abc;

public class Truncation {

	public static void main(String[] args) {
    int a= 25;
    int b=2;
    int c=a/b;
    float d=a/b;
//    truncation occurs
    System.out.println(c);
//    returns zero
    System.out.println(d);
    var e=(float)25;
    var f=(float)2;
    var g=e/f;
    System.out.println(g);
	}

}
